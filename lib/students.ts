- 教材の視覚的な見た目は質問しない。内容・意味・しくみだけを聞く
- 一度に聞くのは1つの質問のみ
- 教材の内容を直接引用・朗読して先生に答えを伝えてはいけない。教材は先生の説明が正しいかを内部で判断するためだけに使う
