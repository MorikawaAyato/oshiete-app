export type Student = {
  id: string
  name: string
  tagline: string
  avatar: ReturnType<typeof require> // 同梱画像（require）。ネットワーク取得しない
  color: string
  colorStrong: string // 白文字を載せるボタン用の濃い色
  tint: string // アバター背景の淡ティント（白いサモエドを映えさせる。落ち着いた色味）
  systemPrompt: string
  endMessage: string
  notebookMessage: string
  notebookThanks: string
  appeal: string
  // プリント授業の定型セリフ（ローカル配信・AIコールなし）
  printGreeting: string // 通常回：宿題プリントを持ってくる
  printGreetingFirst: string // 初回：授業の冒頭で解いたてい
  redpenRequest: string // ✕の問題を聞き始める前置き
  redpenAsk: string // 最初の✕を聞く（{n}=問番号 {q}=問題文）
  redpenAskNext: string // 相づち＋次の✕を聞く（{n} {q}）
  redpenClose: string // 全部聞き終えた締めの相づち
  perfectLine: string // 全問○だったとき
  redpenEcho: string // 締めの前の「教わったことをノートに書いた」の一言（判定はしない、聞いていた証拠だけ。原文引用はしない＝会話的な教え方でも不自然にならない）
  printThanks: string // 授業の締めのお礼
  // ラリー4分類の定型プール（分類だけAI・言葉は手書き）。各文は分類の入力範囲全体（休憩・質問・
  // 暴言・脱線・無意味）に対して、はい/いいえの立場を取らず内容にも触れない検証済みの文にすること
  rallyDontKnow: string[] // わからない・降参への受け（このあと次の問いへ進む）
  rallyPraise: string[] // 褒め・労いへの受け（問いは開いたまま）
  rallyOffTopic: string[] // それ以外への受け流し＋引き戻し（問いは開いたまま）
  rallyAskStudent: string[] // 「君はどう思う？」等、考えを求められたときの受け（{answer}=自分の答案の抜粋を機械差し込み。問いは開いたまま）
  rallyNextAsk: string // 相づちなしで次の✕を聞く（{n} {q}。dont_knowの後用：redpenAskNextの「メモしました」が使えない）
  rallyCloseSoft: string // 1問もメモできずに終わったときの締め（redpenCloseの「ぜんぶメモしました」が使えない）
}

